import { formattingPhones } from "../formattingPhones.js";

describe('проверка на приведение номеров телефонов к единому формату', () => {
    test.each([
        ['8 (927) 000-00-00', '+79270000000'],
        ['7 (927) 000-00-00', '+79270000000'],
        ['86 000 000 0000', '+860000000000'],
        ['+79288106895', '+79288106895'],
        ['  8 (927) 000-00-00   ', '+79270000000'],
        ['8---(927)   000  -- 00 --00 ', '+79270000000'],
        ['8 (927) abc 000 xx 00-00', '+79270000000'],
        ['тел: 8(927)0000000', '+79270000000'],
    ])('tel: %s → %s', (input, expected) => {
        expect(formattingPhones(input)).toBe(expected);
    });
});

